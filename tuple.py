#Tuples
thistuple = ("apple", "banana", "cherry")
print(thistuple)

#Get tuple length
print(len(thistuple))

#Tuple Items - Accept multiple Data Types
tuple1 = ("abc", 34, True, 40, "male")
print(tuple1)

#Access Tuple Items
thistuple = ("apple", "banana", "cherry")
print(thistuple[1])

#Join Two Tuples
tuple1 = ("a", "b" , "c")
tuple2 = (1, 2, 3)

tuple3 = tuple1 + tuple2
print(tuple3)